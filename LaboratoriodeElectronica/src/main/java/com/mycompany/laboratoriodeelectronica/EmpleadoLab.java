    private void cargarDatosSecundarios(String rpe, String tipo){
        switch(tipo){
            case "Becario":                
                fillBecarioPanel(rpe);
                mostrarPanelBecario();
                break;
            case "Colaborador":                
                fillColaboradorPanel(rpe);
                mostrarPanelColab();
                break;
            case "Responsable":                
                fillResponsablePanel(rpe);
                mostrarPanelResp();
                break;
        }
    }
